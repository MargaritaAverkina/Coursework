def translate_label(label: str) -> str:  # Переводимо назву знака на українську
    # Шукаємо переклад у словнику.
    # Якщо перекладу немає — повертаємо оригінальну назву без змін
    translated = LABEL_TRANSLATIONS.get(label)  # Отримуємо переклад з словника
    if translated is None:  # Якщо перекладу немає
        return label  # Повертаємо оригінальну назву
    return f"{translated} ({label})"  # Повертаємо переклад з оригіналом в дужках


def confidence_level(confidence: float) -> str:  # Визначаємо текстовий рівень впевненості
    # Визначаємо текстовий рівень впевненості за числовим значенням
    if confidence >= 0.8:  # Якщо впевненість 80% або вище
        return "висока"  # Повертаємо "висока"
    if confidence >= 0.55:  # Якщо впевненість 55-80%
        return "середня"  # Повертаємо "середня"
    return "низька"  # В інших випадках повертаємо "низька"


